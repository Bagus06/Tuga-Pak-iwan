                                        <h5>Idle Timer Plugin</h5>
                                        <p class="font-size-p">Idle plugin provides you a way to monitor user activity with a page.Idle is defined by not moving the mouse, scrolling the mouse wheel and using the keyboard.</p>

                                        <div class="timer-desc text-center mt-30 mb-30">
                                            <h3>Please do not move the mouse for 5 seconds</h3>
                                            <!-- Icon -->
                                            <div class="timer-icon">
                                                <i class="fa fa-hand-o-up fa-4x"></i>
                                            </div>
                                        </div>
                                        <p>There are two ways to instantiate. Either statically, or on an element. Element bound timers will only watch for events inside of them. You may just want page-level activity, in which case you may set up your timers on <code>document</code>, <code>document.documentElement</code>, and <code>document.body</code>. Instantiate returns jQuery for chaining.</p>

                                        <h6 class="mb-30 mt-30">Example usage</h6>

                                        <!-- preview -->
                                        <div class="timer-pre">
                                            <pre>
$(function()
    // Set idle time
    $( document ).idleTimer( 6000 );
});

$(function()
    $( document ).on( "idle.idleTimer", function(event, elem, obj)
        // function you want to fire when the user goes idle
    });

    $( document ).on( "active.idleTimer", function(event, elem, obj, triggerevent)
        // function you want to fire when the user becomes active again
    });

});</pre>

