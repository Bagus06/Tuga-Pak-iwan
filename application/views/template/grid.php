                                        <h5 class="mb-15">How it works</h5>
                                        <p class="mb-0">How it works Bootstrap’s grid system uses a series of containers, rows, and columns to layout and align content. It’s built with flexbox and is fully responsive. Below is an example and an in-depth look at how the grid comes together.</p>
