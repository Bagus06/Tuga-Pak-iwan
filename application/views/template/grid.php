                                        <div class="show-grid container-fluid">
                                            <div class="container">
                                                <div class="row">
                                                    <div class="col-sm full-border full-padding">
                                                        One of three columns
                                                    </div>
                                                    <div class="col-sm full-border full-padding">
                                                        One of three columns
                                                    </div>
                                                    <div class="col-sm full-border full-padding">
                                                        One of three columns
                                                    </div>
                                                </div>
                                            </div>
